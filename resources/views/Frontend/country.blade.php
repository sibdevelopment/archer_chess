            const itiPopup = window.intlTelInput(popupInput, {
                initialCountry: "", // start blank, user picks from select
                separateDialCode: true,
                preferredCountries: ["in", "us", "gb"],
                utilsScript: "https://cdnjs.cloudflare.com/ajax/libs/intl-tel-input/17.0.19/js/utils.js",
            });

            // optional UX: prevent non-digit chars (except +, space) in the popup input
            $('#mobilePopup').on('input', function() {
                this.value = this.value.replace(/[^\d\s()+-]/g, '');
            })

            $('#confirmbooking-form-header').submit(function(e) {
               e.preventDefault();
               $('div[id$="-error-head"]').empty();
               const selectedCountry = itiPopup.getSelectedCountryData();    

               // Remove existing hidden input (if any)
               $('input[name="country_code"]').remove();

               // Create new hidden country_code input
               if (selectedCountry && selectedCountry.dialCode) {
                   const hiddenInput = $('<input>', {
                       type: 'hidden',
                       name: 'country_code',
                       value: '+' + selectedCountry.dialCode
                   });
                   $(this).append(hiddenInput);
               }
               const url = "{{ route('confirm.trial.class.country') }}";
               const loadingToast = toastr.info('Processing your request...', {
                   timeOut: 0,
                   extendedTimeOut: 0
               });

               $('#modal-loading').modal('show');

               $.ajax({
                   type: "POST",
                   url: url,
                   data: new FormData(this),
                   contentType: false,
                   cache: false,
                   processData: false,
                   success: function(data) {
                       toastr.clear(loadingToast);
                       $('#modal-loading').modal('hide');

                       if (data.status === 'success') {
                           toastr.success(data.message, '', {
                               showMethod: "slideDown",
                               hideMethod: "slideUp",
                               timeOut: 100,
                               closeButton: true,
                               onHidden: function() {
                                   window.location.href =
                                       "{{ route('book.trial.class.thankyou') }}?user_id=" +
                                       data.user_id;
                               }
                           });
                       } else {
                           toastr.error('There is some error!!', '', {
                               showMethod: "slideDown",
                               hideMethod: "slideUp",
                               timeOut: 1500,
                               closeButton: true,
                           });
                       }
                   },
                   error: function(xhr) {
                       toastr.clear(loadingToast);
                       $('#modal-loading').modal('hide');

                       if (xhr.status === 422) {
                           const errors = xhr.responseJSON.errors;
                           let firstErrorField = null;

                           $.each(errors, function(key, value) {
                               const errorDiv = $('#' + key + '-error-head');
                               const inputField = $('[name="' + key + '"]');

                               if (errorDiv.length) {
                                   errorDiv.html(value[0]);
                               } else if (inputField.length) {
                                   inputField.after('<div id="' + key +
                                       '-error" class="text-danger">' + value[0] + '</div>');
                               }

                               if (!firstErrorField) {
                                   firstErrorField = inputField;
                               }
                           });
                       } else {
                           toastr.error('There was an error submitting the form. Please try again.', '', {
                               showMethod: "slideDown",
                               hideMethod: "slideUp",
                               timeOut: 1500,
                               closeButton: true,
                           });
                           console.error("Error submitting form:", xhr);
                       }
                   }
               });
           });

